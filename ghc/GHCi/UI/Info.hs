-- | Info about a module. This information is generated every time a
-- module is loaded.
data ModInfo = ModInfo
    { modinfoSummary    :: !ModSummary
      -- ^ Summary generated by GHC. Can be used to access more
      -- information about the module.
    , modinfoHieFile      :: HieFile
      -- ^ Generated set of information about all spans in the
      -- module that correspond to some kind of identifier for
      -- which there will be type info and/or location info.
    , modinfoInfo       :: !ModuleInfo
      -- ^ Again, useful from GHC for accessing information
      -- (exports, instances, scope) from a module.
    , modinfoLastUpdate :: !UTCTime
      -- ^ The timestamp of the file used to generate this record.
    }

-- | Convenience wrapper around 'srcSpanFile' which results in a 'FilePath'
srcSpanFilePath :: RealSrcSpan -> FilePath
srcSpanFilePath = unpackFS . srcSpanFile

-- | Try to find the location of the given identifier at the given
-- position in the module.
findLoc :: GhcMonad m
        => Map ModuleName ModInfo
        -> RealSrcSpan
        -> String
        -> ExceptT SDoc m (ModInfo,Name,SrcSpan)
findLoc infos span0 string = do
    name  <- maybeToExceptT "Couldn't guess that module name. Does it exist?" $
             guessModule infos (srcSpanFilePath span0)

    info  <- maybeToExceptT "No module info for current file! Try loading it?" $
             MaybeT $ pure $ M.lookup name infos

    name' <- findName infos span0 info string

    case getSrcSpan name' of
        UnhelpfulSpan{} -> do
            throwE ("Found a name, but no location information." <+>
                    "The module is:" <+>
                    maybe "<unknown>" (ppr . moduleName)
                          (nameModule_maybe name'))

        span' -> return (info,name',span')

-- | Searches spans of asts containing a name at a given Name
findNameLocs :: Name -> HieAST TypeIndex -> [RealSrcSpan]
findNameLocs name ast = if contained then (nodeSpan ast) : rest else rest
  where
    contained = M.member (Right name) . nodeIdentifiers . nodeInfo $ ast
    rest = concat . map (findNameLocs name) . nodeChildren $ ast

-- | Find any uses of the given identifier in the codebase.
findNameUses :: (GhcMonad m)
             => Map ModuleName ModInfo
             -> RealSrcSpan
             -> String
             -> ExceptT SDoc m [SrcSpan]
findNameUses infos span0 _string =do
    name  <- maybeToExceptT "Couldn't guess that module name. Does it exist?" $
             guessModule infos (srcSpanFilePath span0)

    info  <- maybeToExceptT "No module info for current file! Try loading it?" $
             MaybeT $ pure $ M.lookup name infos

    case resolveName (modinfoHieFile info) span0 of
        Nothing   -> return []
        Just name -> return $ map RealSrcSpan $ concatMap (search name) (M.elems infos)
  where
    search name otherInfo = searchASTs (modinfoHieFile otherInfo) $ \_ ast -> findNameLocs name ast

-- | Try to resolve the name located at the given position, or
-- otherwise resolve based on the current module's scope.
findName :: GhcMonad m
         => Map ModuleName ModInfo
         -> RealSrcSpan
         -> ModInfo
         -> String
         -> ExceptT SDoc m Name
findName infos span0 mi string =
    case resolveName (modinfoHieFile mi) span0 of
      Nothing -> tryExternalModuleResolution
      Just name ->
        case getSrcSpan name of
          UnhelpfulSpan {} -> tryExternalModuleResolution
          RealSrcSpan   {} -> return (getName name)
  where
    tryExternalModuleResolution =
      case find (matchName $ mkFastString string)
                (fromMaybe [] (modInfoTopLevelScope (modinfoInfo mi))) of
        Nothing -> throwE "Couldn't resolve to any modules."
        Just imported -> resolveNameFromModule infos imported

    matchName :: FastString -> Name -> Bool
    matchName str name =
      str ==
      occNameFS (getOccName name)

-- | Try to resolve the name from another (loaded) module's exports.
resolveNameFromModule :: GhcMonad m
                      => Map ModuleName ModInfo
                      -> Name
                      -> ExceptT SDoc m Name
resolveNameFromModule infos name = do
     modL <- maybe (throwE $ "No module for" <+> ppr name) return $
             nameModule_maybe name

     info <- maybe (throwE (ppr (moduleUnitId modL) <> ":" <>
                            ppr modL)) return $
             M.lookup (moduleName modL) infos

     maybe (throwE "No matching export in any local modules.") return $
         find (matchName name) (modInfoExports (modinfoInfo info))
  where
    matchName :: Name -> Name -> Bool
    matchName x y = occNameFS (getOccName x) ==
                    occNameFS (getOccName y)

searchASTs :: HieFile -> (FastString -> (HieAST TypeIndex) -> [a]) -> [a]
searchASTs hf = concat . flip M.mapWithKey (getAsts $ hie_asts hf)

-- | Try to resolve a source span to the smallest ast containing it
resolveHieAST :: HieFile -> RealSrcSpan -> Maybe (HieAST TypeIndex)
resolveHieAST hf span = listToMaybe $ searchASTs hf $ \_fs ast ->
    maybeToList $ selectSmallestContaining span ast

resolveHieASTAndTypes :: HieFile -> RealSrcSpan -> Maybe (HieAST HieTypeFix)
resolveHieASTAndTypes hf span = fmap (fmap toHieTypeFix) (resolveHieAST hf span)
  where
    toHieTypeFix :: TypeIndex -> HieTypeFix
    toHieTypeFix = (flip recoverFullType (hie_types hf))

getAllTypesHieAST :: HieAST a -> [a]
getAllTypesHieAST ast = directTypes ++ identifierTypes
  where
    directTypes = nodeType . nodeInfo $ ast
    identifierTypes = catMaybes . fmap identType . fmap snd . M.toList . nodeIdentifiers . nodeInfo $ ast

-- | Try to resolve the type display from the given span.
resolveName :: HieFile -> RealSrcSpan -> Maybe Name
resolveName hf span = resolveHieAST hf span >>= listToMaybe . rights . M.keys . nodeIdentifiers . nodeInfo

-- | Try to find the type of the given span.
findType :: GhcMonad m
         => Map ModuleName ModInfo
         -> RealSrcSpan
         -> String
         -> ExceptT SDoc m (ModInfo, Either String HieTypeFix)
findType infos span0 string = do
    name  <- maybeToExceptT "Couldn't guess that module name. Does it exist?" $
             guessModule infos (srcSpanFilePath span0)

    info  <- maybeToExceptT "No module info for current file! Try loading it?" $
             MaybeT $ pure $ M.lookup name infos

    case resolveType (modinfoHieFile info) span0 of
        Nothing -> return (info, Left string)
        Just ty -> return (info, Right ty)
  where
    -- | Try to resolve the type display from the given span.
    resolveType :: HieFile -> RealSrcSpan -> Maybe HieTypeFix
    resolveType hf span = resolveHieASTAndTypes hf span
                             >>= listToMaybe . getAllTypesHieAST

-- | extract all types for @:all-types@ command
allTypes :: HieFile
         -> [(RealSrcSpan, HieTypeFix)]
allTypes hf = searchASTs hf $ \_ ast -> findSrcSpanTypes hf ast

-- | find all HieASTs with a at least one type and return the associated source span
findSrcSpanTypes :: HieFile -> HieAST TypeIndex -> [(RealSrcSpan, HieTypeFix)]
findSrcSpanTypes hf ast = contained hf ast ++ concatMap (findSrcSpanTypes hf) (nodeChildren ast)
  where
    contained :: HieFile -> HieAST TypeIndex -> [(RealSrcSpan, HieTypeFix)]
    contained hf ast =
        let span = nodeSpan ast
            types = hie_types hf
            node_info = nodeInfo ast
            node_identifier_types = catMaybes . fmap identType . fmap snd . M.toList . nodeIdentifiers $ node_info
            node_types = nodeType node_info ++ node_identifier_types
        in  map (\ty -> (span, recoverFullType ty types)) node_types

-- | Guess a module name from a file path.
guessModule :: GhcMonad m
            => Map ModuleName ModInfo -> FilePath -> MaybeT m ModuleName
guessModule infos fp = do
    target <- lift $ guessTarget fp Nothing
    case targetId target of
        TargetModule mn  -> return mn
        TargetFile fp' _ -> guessModule' fp'
  where
    guessModule' :: GhcMonad m => FilePath -> MaybeT m ModuleName
    guessModule' fp' = case findModByFp fp' of
        Just mn -> return mn
        Nothing -> do
            fp'' <- liftIO (makeRelativeToCurrentDirectory fp')

            target' <- lift $ guessTarget fp'' Nothing
            case targetId target' of
                TargetModule mn -> return mn
                _               -> MaybeT . pure $ findModByFp fp''

    findModByFp :: FilePath -> Maybe ModuleName
    findModByFp fp' = fst <$> find ((Just fp' ==) . mifp) (M.toList infos)
      where
        mifp :: (ModuleName, ModInfo) -> Maybe FilePath
        mifp = ml_hs_file . ms_location . modinfoSummary . snd


-- | Collect type info data for the loaded modules.
collectInfo :: (GhcMonad m) => Map ModuleName ModInfo -> [ModuleName]
               -> m (Map ModuleName ModInfo)
collectInfo ms loaded = do
    df <- getDynFlags
    liftIO (filterM cacheInvalid loaded) >>= \case
        [] -> return ms
        invalidated -> do
            liftIO (putStrLn ("Collecting type info for " ++
                              show (length invalidated) ++
                              " module(s) ... "))

            foldM (go df) ms invalidated
  where
    go df m name = do { info <- getModInfo name; return (M.insert name info m) }
                   `gcatch`
                   (\(e :: SomeException) -> do
                         liftIO $ putStrLn
                                $ showSDocForUser df alwaysQualify
                                $ "Error while getting type info from" <+>
                                  ppr name <> ":" <+> text (show e)
                         return m)

    cacheInvalid name = case M.lookup name ms of
        Nothing -> return True
        Just mi -> do
            let fp = srcFilePath (modinfoSummary mi)
                last' = modinfoLastUpdate mi
            current <- getModificationTime fp
            exists <- doesFileExist fp
            if exists
                then return $ current /= last'
                else return True

-- | Get the source file path from a ModSummary.
-- If the .hs file is missing, and the .o file exists,
-- we return the .o file path.
srcFilePath :: ModSummary -> FilePath
srcFilePath modSum = fromMaybe obj_fp src_fp
    where
        src_fp = ml_hs_file ms_loc
        obj_fp = ml_obj_file ms_loc
        ms_loc = ms_location modSum

computeHieFile :: (GhcMonad m) => ModSummary -> TypecheckedModule -> HscEnv -> m HieFile
computeHieFile summary typechecked hscEnv = do
    liftIO . putStrLn $ "Recomputing hie file for " ++ srcFilePath summary
    let (tc_gbl_env, _) = tm_internals_ typechecked
        rn_info = getRenamedStuff tc_gbl_env
    liftIO $ runHsc hscEnv (mkHieFile summary tc_gbl_env (fromJust rn_info))

checkHieFile :: HieFileResult -> Maybe HieFile
checkHieFile hieFileResult = do
    if hie_file_result_version hieFileResult == hieVersion
     then
        Just . hie_file_result $ hieFileResult
     else
        Nothing

getHieFile :: (GhcMonad m) => ModSummary -> TypecheckedModule -> m HieFile
getHieFile m typechecked = do
    withSession $ \hscEnv ->
        case dependencyState (ms_hs_date m) (ms_hie_date m) of
            DSUpToDate -> do
                nc <- liftIO $ readIORef $ hsc_NC hscEnv
                (hieFileResult, ncNew) <- liftIO $ readHieFile nc (ml_hie_file . ms_location $ m)
                let hieFileOpt = checkHieFile hieFileResult
                case hieFileOpt of
                    Just hieFile -> do
                        liftIO . putStrLn $ "Using up to date hie file for " ++ srcFilePath m
                        liftIO $ writeIORef (hsc_NC hscEnv) ncNew
                        return hieFile
                    Nothing -> computeHieFile m typechecked hscEnv
            _ -> computeHieFile m typechecked hscEnv

-- | Get info about the module: summary, types, etc.
getModInfo :: (GhcMonad m) => ModuleName -> m ModInfo
getModInfo name = do
    m <- getModSummary name
    p <- parseModule m
    typechecked <- typecheckModule p
    hieFile <- getHieFile m typechecked
    let i = tm_checked_module_info typechecked
    ts <- liftIO $ getModificationTime $ srcFilePath m
    return (ModInfo m hieFile i ts)

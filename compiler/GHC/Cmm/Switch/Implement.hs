--
-- This module replaces Switch statements as generated by the Stg -> Cmm
-- transformation, which might be huge and sparse and hence unsuitable for
-- assembly code, by proper constructs (if-then-else trees, dense jump tables).
--
-- The actual, abstract strategy is determined by createSwitchPlan in
-- GHC.Cmm.Switch and returned as a SwitchPlan; here is just the implementation in
-- terms of Cmm code. See Note [Cmm Switches, the general plan] in GHC.Cmm.Switch.
--
-- This division into different modules is both to clearly separate concerns,
-- but also because createSwitchPlan needs access to the constructors of
-- SwitchTargets, a data type exported abstractly by GHC.Cmm.Switch.
--

-- | Traverses the 'CmmGraph', making sure that 'CmmSwitch' are suitable for
-- code generation.
cmmImplementSwitchPlans :: DynFlags -> CmmGraph -> UniqSM CmmGraph
cmmImplementSwitchPlans dflags g
    -- Switch generation done by backend (LLVM/C)
    | targetSupportsSwitch (hscTarget dflags) = return g
    | otherwise = do
    blocks' <- concatMapM (visitSwitches dflags (targetPlatform dflags)) (toBlockList g)
    return $ ofBlockList (g_entry g) blocks'

visitSwitches :: DynFlags -> Platform -> CmmBlock -> UniqSM [CmmBlock]
visitSwitches dflags platform block
  | (entry@(CmmEntry _ scope), middle, CmmSwitch vanillaExpr ids) <- blockSplit block
  = let planAndImplementIntegral ts scrutinee = do
          let plan = createSwitchPlan ts
          -- See Note [Floating switch expressions]
          (assignSimple, simpleExpr) <- floatSwitchExpr platform scrutinee

          (newTail, newBlocks) <- implementSwitchPlan platform scope simpleExpr plan

          let block' = entry `blockJoinHead` middle `blockAppend` assignSimple `blockAppend` newTail

          return $ block' : newBlocks

     in case ids of
      CmmByteArraySwitchTargets ts -> do
        (extraBlocks, integralSwitch) <- byteArrayToIntegral
          dflags platform scope vanillaExpr ts
        r <- planAndImplementIntegral integralSwitch
          (cmmLoadIndexW platform vanillaExpr (fixedHdrSizeW dflags) (bWord platform))
        pure (r ++ extraBlocks)
      CmmIntegralSwitchTargets ts -> planAndImplementIntegral ts vanillaExpr
  | otherwise
  = return [block]


-- Note [Floating switch expressions]
-- ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~

-- When we translate a sparse switch into a search tree we would like
-- to compute the value we compare against only once.

-- For this purpose we assign the switch expression to a local register
-- and then use this register when constructing the actual binary tree.

-- This is important as the expression could contain expensive code like
-- memory loads or divisions which we REALLY don't want to duplicate.

-- This happened in parts of the handwritten RTS Cmm code. See also #16933

-- See Note [Floating switch expressions]
floatSwitchExpr :: Platform -> CmmExpr -> UniqSM (Block CmmNode O O, CmmExpr)
floatSwitchExpr _        reg@(CmmReg {})  = return (emptyBlock, reg)
floatSwitchExpr platform expr             = do
  (assign, expr') <- cmmMkAssign platform expr <$> getUniqueM
  return (BMiddle assign, expr')


-- Implementing a switch plan (returning a tail block)
implementSwitchPlan :: Platform -> CmmTickScope -> CmmExpr -> SwitchPlan -> UniqSM (Block CmmNode O C, [CmmBlock])
implementSwitchPlan platform scope expr = go
  where
    go (Unconditionally l)
      = return (emptyBlock `blockJoinTail` CmmBranch l, [])
    go (JumpTable ids)
      = let intIds = CmmIntegralSwitchTargets ids in
        return (emptyBlock `blockJoinTail` CmmSwitch expr intIds, [])
    go (IfLT signed i ids1 ids2)
      = do
        (bid1, newBlocks1) <- go' ids1
        (bid2, newBlocks2) <- go' ids2

        let lt | signed    = cmmSLtWord
               | otherwise = cmmULtWord
            scrut = lt platform expr $ CmmLit $ mkWordCLit platform i
            lastNode = CmmCondBranch scrut bid1 bid2 Nothing
            lastBlock = emptyBlock `blockJoinTail` lastNode
        return (lastBlock, newBlocks1++newBlocks2)
    go (IfEqual i l ids2)
      = do
        (bid2, newBlocks2) <- go' ids2

        let scrut = cmmNeWord platform expr $ CmmLit $ mkWordCLit platform i
            lastNode = CmmCondBranch scrut bid2 l Nothing
            lastBlock = emptyBlock `blockJoinTail` lastNode
        return (lastBlock, newBlocks2)

    -- Same but returning a label to branch to
    go' (Unconditionally l)
      = return (l, [])
    go' p
      = do
        bid <- mkBlockId `fmap` getUniqueM
        (last, newBlocks) <- go p
        let block = CmmEntry bid scope `blockJoinHead` last
        return (bid, block: newBlocks)

byteArrayToIntegral ::
     DynFlags
  -> Platform
  -> CmmTickScope
  -> CmmExpr
  -> ByteArraySwitchTargets
  -> UniqSM ([CmmBlock], SwitchTargets)
byteArrayToIntegral dflags platform scope scrut0 ts = do
  let ByteArraySwitchPlan defTarget m = createByteArraySwitchPlan ts
  (integralTable,integralBranches) <- foldlM
    ( \(itable,blocks) strat -> case strat of
      OneNoHash tgt -> do
        let len = ByteString.length (bastKey tgt)
        bid <- mkBlockId `fmap` getUniqueM
        let scrut = cmmOffset platform scrut0 (arrWordsHdrSize dflags)
        (testEqNode,res) <- regEqualsLit platform scrut (CmmLit (bastKeyLit tgt)) len
        let condNode = CmmCondBranch
              (eqWordExpr platform
                (CmmReg (CmmLocal res))
                (CmmLit (mkIntCLit platform 0))
              )
              (bastTarget tgt)
              defTarget
              (Just True)
        bid <- mkBlockId `fmap` getUniqueM
        let block = CmmEntry bid scope `blockJoinHead` (testEqNode `blockJoinTail` condNode)
        return (M.insert (fromIntegral len) bid itable, block : blocks)
      ManyHash{} -> error "byteArrayToIntegral: write this code"
    ) (M.empty,[]) m
  let low = maybe 0 (fst.fst) (M.minViewWithKey m)
  let high = maybe 0 (fst.fst) (M.maxViewWithKey m)
  pure
    ( integralBranches
    , mkSwitchTargets False (fromIntegral low,fromIntegral high)
        (Just defTarget) integralTable
    )

orWordExpr :: Platform -> CmmExpr -> CmmExpr -> CmmExpr
orWordExpr platform a b = CmmMachOp (mo_wordOr platform) [a,b]

xorWordExpr :: Platform -> CmmExpr -> CmmExpr -> CmmExpr
xorWordExpr platform a b = CmmMachOp (mo_wordXor platform) [a,b]

eqWordExpr :: Platform -> CmmExpr -> CmmExpr -> CmmExpr
eqWordExpr platform a b = CmmMachOp (mo_wordEq platform) [a,b]

upcastW8 :: Platform -> CmmExpr -> CmmExpr
upcastW8 platform c = CmmMachOp (mo_u_8ToWord platform) [c]

-- Returns a new register where the result is written.
regEqualsLit ::
     Platform
  -> CmmExpr -- pointer to beginning of byte array payload (scrutinee)
  -> CmmExpr -- pattern being checked against
  -> Int -- length of both scrutinee and pattern
  -> UniqSM (Block CmmNode O O, LocalReg)
regEqualsLit platform scrut pat n = do
    resultRegId <- getUniqueM
    let res = LocalReg resultRegId (bWord platform)
        initialize = CmmAssign (CmmLocal res) (CmmLit (mkIntCLit platform 0))
        unrolledEquality ix = if ix < n
          then 
            BMiddle
              ( CmmAssign (CmmLocal res) $ orWordExpr platform
                (CmmReg (CmmLocal res))
                ( xorWordExpr platform
                  (upcastW8 platform (CmmLoad (cmmOffset platform pat ix) b8))
                  (upcastW8 platform (CmmLoad (cmmOffset platform scrut ix) b8))
                )
              )
            `blockAppend`
            unrolledEquality (ix + 1)
          else emptyBlock
    pure (BMiddle initialize `blockAppend` unrolledEquality 0, res)
